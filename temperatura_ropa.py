
temperatura = float(input("Ingresa la temperatura: "))

if temperatura <= 0:
    print("LLevate la cobija de tigre")
elif temperatura <= 20:
    print("venir abrigado")
elif temperatura <= 30:
    print("ropa comoda")
elif temperatura <= 40:
    print("ropa holgada y ventilada")
else:
    print("desnudo")